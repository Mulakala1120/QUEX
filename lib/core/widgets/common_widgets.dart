class QueXLogo extends StatelessWidget {
  const QueXLogo({super.key, this.size = 48, this.showWordmark = false});

  final double size;
  final bool showWordmark;

  @override
  Widget build(BuildContext context) {
    return QueXBrandLogo(size: size, showWordmark: showWordmark);
  }
}

class QueXAppBar extends StatelessWidget implements PreferredSizeWidget {
  const QueXAppBar({
    super.key,
    required this.title,
    this.actions,
    this.showBack = true,
  });

  final String title;
  final List<Widget>? actions;
  final bool showBack;

  @override
  Size get preferredSize => const Size.fromHeight(kToolbarHeight);

  @override
  Widget build(BuildContext context) {
    return AppBar(
      title: Text(title),
      automaticallyImplyLeading: showBack,
      actions: actions,
    );
  }
}

class PrimaryButton extends StatelessWidget {
